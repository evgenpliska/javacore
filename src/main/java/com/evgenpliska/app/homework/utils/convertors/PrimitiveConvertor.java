public class PrimitiveConvertor {

    public char floatToChar(float myFloat){
        return (char) myFloat;
    }

    public char intToChar(int myInt){
        return (char) myInt;
    }

    public int charToInt(char myChar){
        return (int) myChar;
    }
}
